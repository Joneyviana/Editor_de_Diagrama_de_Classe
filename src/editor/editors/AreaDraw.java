package editor.editors;

public class AreaDraw {

public int x ;
public int y ;
public int width;
public int height;
public int scale_reducao ;
public int redimensionamento ;
public AreaDraw(int x ,int y ,int width,int height,int scale_reducao,int redimensionamento){
	this.x =x;
	this.y =y ;
	this.width = width ;
	this.height = height;
    this.scale_reducao = scale_reducao;
    this.redimensionamento = redimensionamento;
}
}
